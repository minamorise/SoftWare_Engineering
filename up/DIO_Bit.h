/* ******************************************************************** */
/* 					SET_BIT by high value	     				        */
/* by shifting 1 by the number of bits then oring this bit with 	    */
/* 0 or 1 the result will be 1  any way 							    */
/* ******************************************************************** */
#define SET_BIT(var,bit_num) (var|=(1<<bit_num))






/* ******************************************************************** */
/* 					SET_BYTE by high value	     				        */
/* set all bits of the byte with 1 value by oring the byte with 0xff    */
/* ******************************************************************** */
#define SET_BYTE(var) (var=(0xff))






/* ********************************************************************     */
/* 					CLEAR_BIT by "bit=0" 	     				            */
/*req: to make the value of a specific bit by 0 regardless its initial      */
/* value 																    */
/* this happens by : shifting 1 by bit number the not(1) = 0 then anding it */
/* ********************************************************************     */
#define CLEAR_BIT(var,bit_num)  (var)&=( ~ (1 <<(bit_num) ))






/* ******************************************************************** */
/* 					CLER_BYTE by "BYTE=0000 0000" 	     				*/
/* ******************************************************************** */
